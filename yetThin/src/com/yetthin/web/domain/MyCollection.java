package com.yetthin.web.domain;

public class MyCollection {
    private Integer collectionId;

    private String collectionGroupId;

    private String collectionUserId;

    public Integer getCollectionId() {
        return collectionId;
    }

    public void setCollectionId(Integer collectionId) {
        this.collectionId = collectionId;
    }

    public String getCollectionGroupId() {
        return collectionGroupId;
    }

    public void setCollectionGroupId(String collectionGroupId) {
        this.collectionGroupId = collectionGroupId;
    }

    public String getCollectionUserId() {
        return collectionUserId;
    }

    public void setCollectionUserId(String collectionUserId) {
        this.collectionUserId = collectionUserId;
    }
}