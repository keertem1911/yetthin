package com.yetthin.web.domain;

public class ReasonWord {
    private String reasonwordId;

    private String reasonwordGroupId;

    private String reasonwordContent;

    public String getReasonwordId() {
        return reasonwordId;
    }

    public void setReasonwordId(String reasonwordId) {
        this.reasonwordId = reasonwordId;
    }

    public String getReasonwordGroupId() {
        return reasonwordGroupId;
    }

    public void setReasonwordGroupId(String reasonwordGroupId) {
        this.reasonwordGroupId = reasonwordGroupId;
    }

    public String getReasonwordContent() {
        return reasonwordContent;
    }

    public void setReasonwordContent(String reasonwordContent) {
        this.reasonwordContent = reasonwordContent;
    }
}